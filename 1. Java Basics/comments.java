/**
 * This is the documentation comment for the class.
 * Class Name: comments
 * Purpose: To demonstrate single-line, multi-line, and documentation comments.
 */
public class comments {

    public static void main(String[] args) {
        
        // This is a single-line comment
        System.out.println("Single-line comment example.");

        /*
         * This is a multi-line comment.
         * It can span multiple lines.
         * Used for explaining blocks of code.
         */
        System.out.println("Multi-line comment example.");

        /**
         * This is a documentation comment.
         * It is used to generate external documentation using Javadoc.
         * Commonly used before classes and methods.
         */
        System.out.println("Documentation comment example.");
    }
}
